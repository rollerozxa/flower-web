<p>So essentially, the game has two currencies, <b>seeds</b> and <b>stars</b>. They can be used to buy items, spend it on draws, or really anything else in this game. There's a third currency, but you don't need to know about it at the moment. <i>See chapter 7 for PGM.</i></p>

<p>You get <b>seeds</b> mainly from your hourly seed income, but you can also get it from winning seeds draws and reporting bugs.</p>

<p><b>Stars</b> is the second currency. Stars are considered more valuable, and is harder to obtain.</p>

<p><b>Stars</b> can be obtained by adding friends, as each friend gives you 1 star per hour.</p>
